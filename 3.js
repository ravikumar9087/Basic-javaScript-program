// rules for naming variables

// you cant't start with number
// example:-
// 1value (invalid)
// value1 (valid)

var value = 3;
console.log(value**5);

// you can use only undersore _ or dollar symbol
// first_name(valid)
// _firstname(valid)

var first_name = 'Ravi'; //snake case writting
var _firstname = 'Ravi';

console.log(first_name);
console.log(_firstname);
// first$name(valid)
// $firstname(valid)

var first$name = 'Ravi';
var $firstname = 'Ravi';

console.log(first$name);
console.log($firstname);
// you can't use spaces

var firstName = 'Ravi' //camel case writting
console.log(firstName);

